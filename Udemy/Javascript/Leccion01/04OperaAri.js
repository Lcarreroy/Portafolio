// suma

let a = 3, b = 2;
let suma = a + b;
console.log("Resultado de la suma: " + suma);

//Resta
let resta = a - b;
console.log("Resultado de la suma: " + resta);

//Multiplicacion
let multiplicacion = a * b;
console.log("Resultado de la suma: " + multiplicacion);

//Divicion
let divicion = a / b;
console.log("Resultado de la suma: " + divicion);

// modulo, residuo de la divicion
let modulo = a % b;
console.log("Resultado de la suma: " + modulo);

let exponecial = a ** b;
console.log("Resultado de la suma: " + exponecial);

